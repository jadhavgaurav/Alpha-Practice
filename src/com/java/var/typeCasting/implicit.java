package com.java.var.typeCasting;

import java.util.Scanner;

public class implicit {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);

		int number = sc.nextInt();
		float num2 = number;
		System.out.println();

//		type conversion
		// Also called implicit Type Casting
		/*
		byte=>
		short=>
		int=>
		float=>
		long=>
		double.
		 */
	}
}
